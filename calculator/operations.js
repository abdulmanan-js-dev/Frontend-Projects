const addition = (val1, val2) => {
    return val1 + val2;
};

const subtraction = (val1, val2) => {
    return val1 - val2;
};

const multiplacation = (val1, val2) => {
    return val1 * val2;
};

const division = (val1, val2) => {
    return val1 / val2;
};

export { addition, subtraction, multiplacation, division };
